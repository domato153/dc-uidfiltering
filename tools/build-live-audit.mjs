import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const probePath = path.join(rootDir, 'testbed', 'live-comment-flicker-probe.user.js');
const profilerSourcePath = path.join(rootDir, 'tools', 'dcuf-view-load-profiler.source.js');
const outputPath = path.join(rootDir, 'tools', 'dcuf-view-load-profiler.user.js');

function splitUserscript(text, label) {
    const match = text.match(/^(\/\/ ==UserScript==\r?\n[\s\S]*?^\/\/ ==\/UserScript==)\r?\n?/m);
    if (!match || match.index !== 0) throw new Error(`${label}: userscript metadata block is missing`);
    return {
        metadata: match[1],
        body: text.slice(match[0].length).trim()
    };
}

const [probeText, profilerText] = await Promise.all([
    readFile(probePath, 'utf8'),
    readFile(profilerSourcePath, 'utf8')
]);
const probe = splitUserscript(probeText, 'comment probe');
const profiler = splitUserscript(profilerText, 'page profiler');
const output = [
    profiler.metadata,
    '',
    '// Generated by tools/build-live-audit.mjs. Edit the two source files, not this output.',
    '',
    probe.body,
    '',
    profiler.body,
    ''
].join('\n');

await writeFile(outputPath, output, 'utf8');
console.log(`Built ${path.relative(rootDir, outputPath)}`);
